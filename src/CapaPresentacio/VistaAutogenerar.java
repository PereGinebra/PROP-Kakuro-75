    private void BotoOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotoOKActionPerformed
        int nFiles = SliderRows.getValue();
        int nColumnes = SliderColumns.getValue();
        String dif = "";
        boolean difTriada = true;
        if (BotoEasy.isSelected()){dif = "facil";}
        else if (BotoMedium.isSelected()){dif = "mig";}
        else if (BotoHard.isSelected()){dif = "dificil";}
        else {difTriada = false; LabelAvis.setText("Please choose a difficulty");}

        if (difTriada){
            
            cp.generarIGuardarRandomKakuro(dif,nFiles,nColumnes);
            VistaPartida VP = new VistaPartida(this,cp);
            VP.setVisible(true);
            this.setVisible(false);
              
            
        }
    }//GEN-LAST:event_BotoOKActionPerformed

