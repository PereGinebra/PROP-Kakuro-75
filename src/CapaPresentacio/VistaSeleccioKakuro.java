    private void botoNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botoNextActionPerformed
        if(!llista.isSelectionEmpty()) {
            System.out.println(llista.getSelectedValue());
            if (!tipus[2]){
                if (tipus[0]){
                cp.carregarKakuro(llista.getSelectedValue());
                VistaPartida partida = new VistaPartida(parent, cp);
                partida.setVisible(true);
                dispose();
                }
                
                else{
                cp.carregarPartida(llista.getSelectedValue());
                VistaPartida partida = new VistaPartida(parent, cp);
                partida.setVisible(true);
                dispose();
                }
            }
            
            else {
                VistaRankingPartida ranking = new VistaRankingPartida(parent,cp, llista.getSelectedValue());
                ranking.setVisible(true);
                dispose();
            }
        }
        
        else {
            missatge.setText("*You have to select a Kakuro*");
        }
    }//GEN-LAST:event_botoNextActionPerformed


