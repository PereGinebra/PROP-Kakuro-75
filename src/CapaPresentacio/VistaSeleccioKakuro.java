    private void cleanList(){
        DefaultListModel model = new DefaultListModel();
        llista.setModel(model);
        
    }
    
    private void addData(){
        DefaultListModel model = (DefaultListModel) llista.getModel();
        
        if (facil) {
            for (int i = 0; i < facils.length; i++) { model.addElement(facils[i]);}
        }
            
        if (mig) {
            for (int i = 0; i < mitjans.length; i++) { model.addElement(mitjans[i]);}
        }
            
        if (dificil) {
            for (int i = 0; i < dificils.length; i++) { model.addElement(dificils[i]);}
        }
        
    }
    
    private void actualitzarLlista(){
        cleanList();
        addData();        
    }
    
