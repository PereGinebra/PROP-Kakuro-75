    private void setRanking() {
        String[][] ranking = cp.rankingUsuaris();
        
        for (int i = 0; i < ranking.length; i++) {
            if (i == 0) {
                user1.setText(ranking[i][0]);
                points1.setText(ranking[i][1]);
                
            }
            
            else if (i == 1) {
                user2.setText(ranking[i][0]);
                points2.setText(ranking[i][1]);
                
            }
            
            else if (i == 2) {
                user3.setText(ranking[i][0]);
                points3.setText(ranking[i][1]);
                
            }
            
            else if (i == 3) {
                user4.setText(ranking[i][0]);
                points4.setText(ranking[i][1]);
                
            }
            
            else if (i == 4) {
                user5.setText(ranking[i][0]);
                points5.setText(ranking[i][1]);
                
            }
        }
    }


